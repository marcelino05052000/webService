package com.mycompany.projeto;

import java.util.Objects;
import javax.persistence.Id;

/**
 *
 * @author Marcelino
 */
public class Pessoa {
    @Id
    String nome;
    int idade;

    public Pessoa(String nome, int idade) {
        this.nome = nome;
        this.idade = idade;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public int getIdade() {
        return idade;
    }

    public void setIdade(int idade) {
        this.idade = idade;
    }

}
